public class Aula06EstruturaDeRepetição03 {
    public static void main(String[] args) {
        int valorMax = 50;
        for(int i=0;i <= valorMax; i++){
            if (i>25) {
                break;
            }
            System.out.println(i);
        }
    }
    // imrpima os primeiros 25 numreos de um dado valor. por exemplo, 50

}
